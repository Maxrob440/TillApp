CREATE TABLE IF NOT EXISTS "ORDERS" (
    "ID" INTEGER PRIMARY KEY AUTOINCREMENT,
    "ORDER_ID" INTEGER,
    "ITEM_ID" INTEGER,
    "QUANTITY" INTEGER,
    "TOTAL_PRICE" REAL DEFAULT 0,
    "ORDER_DATE" DATETIME DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY("ITEM_ID") REFERENCES "ITEMS"("ITEM_ID")
);


CREATE TRIGGER IF NOT EXISTS update_total_price_after_insert
AFTER INSERT ON ORDERS
FOR EACH ROW
BEGIN
    -- Update the total price for all rows in ORDERS based on the ITEM_ID and QUANTITY
    UPDATE ORDERS
    SET TOTAL_PRICE = (SELECT PRICE FROM ITEMS WHERE ITEM_ID = ORDERS.ITEM_ID) * ORDERS.QUANTITY;
END;



CREATE TABLE IF NOT EXISTS "ITEMS"(
    "ITEM_ID" INTEGER PRIMARY KEY AUTOINCREMENT,
    "ITEM_NAME" TEXT,
    "PRICE" REAL);

